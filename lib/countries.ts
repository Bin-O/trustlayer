// 表記ゆれを正規化してCOUNTRIESの表記を返す。リストに無ければnull（呼び出し側で「その他」へ）
export function normalizeNationality(value: string): string | null {
  const v = value.trim()
  if (!v) return null
  if (COUNTRIES.includes(v)) return v
  const alias = NATIONALITY_ALIASES[v]
  if (alias) return alias
  return null
}

